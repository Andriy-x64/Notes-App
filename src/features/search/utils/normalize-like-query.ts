/**
 * Утиліта нормалізації пошукового запиту для оператора LIKE.
 * Екранує символи \, % і _ для безпечного використання у шаблоні LIKE.
 * Частковий збіг формується там, де до екранізованого запиту додаються символи %.
 */
export const normalizeLikeQuery = (query: string) => {
  return query.trim().replace(/[\\%_]/g, (character) => `\\${character}`);
};
